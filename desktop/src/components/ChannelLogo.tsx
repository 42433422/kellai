/** Logo 组件映射 */
const LOGO_MAP: Record<string, React.FC<{ size: number }>> = {
  wechat: WechatLogo,
  wework: WeworkLogo,
  douyin: DouyinLogo,
  miniprogram: MiniprogramLogo,
  pdd: PddLogo,
  taobao: TaobaoLogo,
  jd: JdLogo,
  alibaba: AlibabaLogo,
  whatsapp: WhatsappLogo,
  telegram: TelegramLogo,
  line: LineLogo,
  phone: PhoneLogo,
  email: EmailLogo,
  sms: SmsLogo,
  web: WebLogo,
};

/** 渠道品牌 Logo 组件 */
export default function ChannelLogo({ type, className, size = 20 }: ChannelLogoProps) {
  const Logo = LOGO_MAP[type];
  if (Logo) {
    return (
      <span className={className} style={{ display: 'inline-flex', alignItems: 'center', justifyContent: 'center' }}>
        <Logo size={size} />
      </span>
    );
  }
  // fallback: 用品牌色圆角方块 + 首字母
  const color = CHANNEL_BRAND_COLOR[type] ?? '#6B7280';
  const label = {
    wechat: '微', wework: '企', phone: '电', douyin: '抖', miniprogram: '小',
    email: '邮', sms: '信', web: '网', pdd: '拼', taobao: '淘',
    jd: '京', alibaba: '1', whatsapp: 'W', telegram: 'T', line: 'L',
  }[type] ?? type[0]?.toUpperCase() ?? '?';

  return (
    <span
      className={className}
      style={{
        display: 'inline-flex',
        alignItems: 'center',
        justifyContent: 'center',
        width: size,
        height: size,
        borderRadius: size * 0.25,
        background: color,
        color: 'white',
        fontSize: size * 0.5,
        fontWeight: 700,
        fontFamily: 'system-ui, sans-serif',
      }}
    >
      {label}
    </span>
  );
}
